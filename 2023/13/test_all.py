#!/usr/bin/env python3
import pytest
from main import *

@pytest.mark.parametrize(
    "m, expected",
    [
        (['#.##..##.',
          '..#.##.#.',
          '##......#',
          '##......#',
          '..#.##.#.',
          '..##..##.',
          '#.#.##.#.'],
         (4, False)),

        (['####..##.',
          '..#.##.#.',
          '##......#',
          '##......#',
          '..#.##.#.',
          '..##..##.',
          '..#.##.#.'],
         (0, False)),

        (['#.##..#..',
          '..#.##...',
          '#......##',
          '#......##',
          '..#.##...',
          '..##..#..',
          '..#.##...'],
         (7, False)),

        (['#...##..#',
          '#....#..#',
          '..##..###',
          '#####.##.',
          '#####.##.',
          '..##..###',
          '#....#..#'],
         (3, True)),

        (['#...##..#',
          '#....#..#',
          '..##..###',
          '#####..#.',
          '.####.##.',
          '..##..###',
          '..##..###'],
         (5, True)),

        (['#...##..#',
          '#...##..#',
          '..##..###',
          '#####..#.',
          '.####.##.',
          '..##...##',
          '..##.####'],
         (0, True)),
                         ]
)
def test_find_mirror(m, expected):
    assert expected == find_mirror(m)


@pytest.mark.parametrize(
    "m, expected",
    [
        ([[1, 2, 3],
          [4, 5, 6]],
         [[1, 4],
          [2, 5],
          [3, 6]])
    ]
)
def test_transpose(m, expected):
    assert expected == transpose(m)
