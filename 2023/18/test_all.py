from main import *

def test_flood_fill():
    m = \
        [['#', '#', '#', '#', '#', '#', '#'],
         ['#', '.', '.', '.', '.', '.', '#'],
         ['#', '#', '#', '.', '.', '.', '#'],
         ['.', '.', '#', '.', '.', '.', '#'],
         ['.', '.', '#', '.', '.', '.', '#'],
         ['#', '#', '#', '.', '#', '#', '#'],
         ['#', '.', '.', '.', '#', '.', '.'],
         ['#', '#', '.', '.', '#', '#', '#'],
         ['.', '#', '.', '.', '.', '.', '#'],
         ['.', '#', '#', '#', '#', '#', '#']]

    expected = \
        [['#', '#', '#', '#', '#', '#', '#'],
         ['#', '#', '#', '#', '#', '#', '#'],
         ['#', '#', '#', '#', '#', '#', '#'],
         ['.', '.', '#', '#', '#', '#', '#'],
         ['.', '.', '#', '#', '#', '#', '#'],
         ['#', '#', '#', '#', '#', '#', '#'],
         ['#', '#', '#', '#', '#', '.', '.'],
         ['#', '#', '#', '#', '#', '#', '#'],
         ['.', '#', '#', '#', '#', '#', '#'],
         ['.', '#', '#', '#', '#', '#', '#']]

    access = lambda m, x, y: m[y][x]

    def inside(m, x, y):
        return m[y][x] == '.'

    def setf(m, x, y):
        m[y][x] = '#'

    flood_fill(m, (1, 1), setf, inside)  # modify in-place

    assert expected == m


